    <p>
  Athleisure is a modern style that blurs the line between athletic wear and everyday fashion. It took shape in the early 2000s as sportswear brands like Nike, Adidas, and Lululemon began designing clothing that was functional for workouts but stylish enough for casual settings. Its rise coincided with the global push for wellness and the popularity of fitness culture, yoga, and gym memberships.
</p>

<p>
  The foundation of athleisure is comfort — leggings, joggers, sports bras, hoodies, and sneakers are designed with breathable, high-performance fabrics yet worn in everyday life, from cafés to offices. The look was accelerated by celebrity influence, with figures like Beyoncé, Kanye West, and the Kardashian–Jenner family normalizing gym-inspired fashion as street-ready attire.
</p>

<p>
  Today, athleisure reflects the broader shift toward hybrid lifestyles: working, exercising, and socializing seamlessly throughout the day. It emphasizes minimalism, clean lines, and technical fabrics, becoming not just a trend but a cultural staple in the 21st-century wardrobe.
